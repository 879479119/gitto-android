package com.stormphoenix.ogit.log;

public class AB {

    private int testId;

    private int paramId;

    public AB(int testId, int paramId) {
        this.testId = testId;
        this.paramId = paramId;
    }

    public int getTestId() {
        return testId;
    }

    public void setTestId(int testId) {
        this.testId = testId;
    }

    public int getParamId() {
        return paramId;
    }

    public void setParamId(int paramId) {
        this.paramId = paramId;
    }
}
