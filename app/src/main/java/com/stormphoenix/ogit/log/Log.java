package com.stormphoenix.ogit.log;

public class Log {

    private Base base;

    private AB ab;

    private Detail detail;

    public Base getBase() {
        return base;
    }

    public void setBase(Base base) {
        this.base = base;
    }

    public AB getAb() {
        return ab;
    }

    public void setAb(AB ab) {
        this.ab = ab;
    }

    public Detail getDetail() {
        return detail;
    }

    public void setDetail(Detail detail) {
        this.detail = detail;
    }
}
